const soma = function(x,y){
    return Number(x) + Number(y);
};
const subtracao = function(x,y){
    return Number(x) - Number(y);
};
const multiplicacao = function(x,y){
    return Number(x) * Number(y);
}
const divisao = function(x,y){
    return Number(x) / Number(y);
}
const resto = function(x,y){
    return Number(x) % Number(y);
}
const potencia = function(x,y){
    return Number(x) ** Number(y);
}